def parse_header_body(code):
    """
    parse c header description and program body
    :param code:
    :return:
    """
    headers = ""
    body = code
    # impl
    return headers, body


def construct_program(headers, body):
    """
    construct c source code from c header description and program body
    :param headers:
    :param body:
    :return:
    """
    # impl
    return headers + body
